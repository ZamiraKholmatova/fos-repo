def num(value: float, decimals: int = 0) -> str:
    """Форматирует число с неразрывным разделением разрядов пробелом."""
    return f"{value:,.{decimals}f}".replace(",", "\u00a0")


def make_variant(index: int, rng: random.Random) -> dict:
    """Формирует один вариант контрольной работы."""
    domain, task = rng.choice(DOMAINS)
    params = ProjectParameters(
        volume_per_year=rng.choice([60_000, 80_000, 100_000, 120_000, 150_000, 200_000]),
        manual_cost=float(rng.choice([35, 40, 45, 55, 60, 70, 85])),
        accuracy=rng.choice([0.86, 0.88, 0.90, 0.92, 0.94]),
        automation_rate=rng.choice([0.55, 0.60, 0.65, 0.70, 0.75]),
        capex=float(rng.choice([2_000_000, 2_500_000, 3_000_000, 3_500_000, 4_500_000])),
        opex_per_year=float(rng.choice([600_000, 750_000, 900_000, 1_100_000])),
        error_cost=float(rng.choice([90, 120, 150, 180, 250])),
        horizon_years=3,
    )
    questions = rng.sample(CLOSED_QUESTIONS, 3)
    return {"index": index, "domain": domain, "task": task,
            "params": params, "questions": questions, "result": calculate(params)}


def render(variant: dict, with_answers: bool) -> str:
    p, r = variant["params"], variant["result"]
    lines = [f"## Вариант {variant['index']}", "",
             "### Часть 1. Теоретические вопросы (3 × 1 балл)", ""]
    for i, (question, answer) in enumerate(variant["questions"], 1):
        lines.append(f"{i}. {question}")
        if with_answers:
            lines.append(f"   > *Эталон:* {answer}")
        lines.append("")

    lines += ["### Часть 2. Расчетная задача (2 балла)", "",
              f"Организация рассматривает внедрение системы ИИ для обработки объектов "
              f"«{variant['domain']}». Решаемая задача — {variant['task']}.", "",
              "Исходные данные:", "",
              f"| Параметр | Значение |", "|---|---:|",
              f"| Объем операций в год, шт. | {num(p.volume_per_year)} |",
              f"| Стоимость ручной обработки одной операции, руб. | {p.manual_cost:.0f} |",
              f"| Ожидаемая точность модели | {p.accuracy:.0%} |",
              f"| Доля операций, обрабатываемых без участия человека | {p.automation_rate:.0%} |",
              f"| Стоимость исправления одной ошибки модели, руб. | {p.error_cost:.0f} |",
              f"| Единовременные затраты на разработку и внедрение, руб. | {num(p.capex)} |",
              f"| Периодические затраты, руб./год | {num(p.opex_per_year)} |",
              f"| Горизонт оценки, лет | {p.horizon_years} |", "",
              "**Требуется:**", "",
              "1. Рассчитать затраты при полностью ручной обработке (базовый вариант).",
              "2. Рассчитать чистую годовую экономию с учетом остаточной ручной обработки, "
              "потерь от ошибок модели и периодических затрат.",
              "3. Определить срок окупаемости.",
              "4. Указать не менее двух рисков, способных обесценить расчет, и предложить меры "
              "реагирования.", ""]

    if with_answers:
        payback = f"{r.payback_years:.2f} года" if r.payback_years else "не окупается"
        lines += ["> **Эталонное решение**", ">",
                  f"> 1. Базовые затраты: {num(p.volume_per_year)} × {p.manual_cost:.0f} = "
                  f"{num(r.baseline_cost)} руб./год",
                  f"> 2. Остаточная ручная обработка: {num(r.residual_manual_cost)} руб./год; "
                  f"потери от ошибок: {num(r.error_losses)} руб./год; "
                  f"чистая экономия: {num(r.net_saving)} руб./год",
                  f"> 3. Срок окупаемости: {num(p.capex)} / {num(r.net_saving)} = {payback}",
                  "> 4. Риски: недостижение заявленной точности на реальных данных; деградация "
                  "модели и рост доли ручной проверки; недооценка стоимости ошибки; отсутствие "
                  "размеченных данных. Меры: пилот на ограниченном объеме, мониторинг качества с "
                  "порогом переобучения, резерв бюджета, проверка доступности данных до старта.", ""]
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Генератор вариантов КИМ-2.4")
    parser.add_argument("--count", type=int, default=8, help="число вариантов")
    parser.add_argument("--seed", type=int, default=2026, help="зерно генератора")
    parser.add_argument("--with-answers", action="store_true", help="включить эталонные ответы")
    parser.add_argument("--out", help="файл для записи (по умолчанию — вывод на экран)")
    args = parser.parse_args()

    rng = random.Random(args.seed)
    header = [f"# Контрольная работа (КИМ-2.4). Варианты", "",
              f"Сгенерировано: `generate_test_variants.py --count {args.count} --seed {args.seed}`  ",
              f"Для восстановления комплекта используйте то же значение `--seed`.", ""]
    if args.with_answers:
        header += ["> **Экземпляр преподавателя: содержит эталонные ответы. "
                   "Не публиковать в открытой части репозитория.**", ""]
    body = [render(make_variant(i, rng), args.with_answers) for i in range(1, args.count + 1)]
    text = "\n".join(header) + "\n---\n\n".join(body)

    if args.out:
        Path(args.out).write_text(text, encoding="utf-8")
        print(f"Записано вариантов: {args.count} -> {args.out}")
    else:
        print(text)


if __name__ == "__main__":
    main()
